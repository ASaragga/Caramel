"""
    _rand_header()

Chooses a random header.
"""
function _rand_header()
    return rand(HEADERS)
end;

"""
    get_cookie()

Retrieves cookies from "https://fc.yahoo.com".
"""
function get_cookie()
    res = HTTP.get("https://fc.yahoo.com",cookies=true,redirect=true,_HEADER,status_exception = false)
    cookies = HTTP.cookies(res)
    return Dict(cookies[1].name => cookies[1].value)
end;

"""
    get_crumb()

Passess the request header and the cookies to "https://query2.finance.yahoo.com/v1/test/getcrumb" and retrieves the crumb. 
If the global _HEADER or _COOKIE variables are not defined they are created.
"""
function get_crumb()
    if (@isdefined _HEADER) && (@isdefined _COOKIE)
        nothing
    else
        _set_cookies_and_crumb()
    end
    crumb = HTTP.get("https://query2.finance.yahoo.com/v1/test/getcrumb",_HEADER,cookies=_COOKIE)
    res = String(crumb.body)
    if isequal(res,"")
        @warn "Crumb could not be retrieved. Certain data items will not be available!"
    end
    return res
end;

"""
    _renew_cookies_and_crumb()

Renews both the cookies and the crumb.
"""
function _renew_cookies_and_crumb()
    if @isdefined _HEADER
       global _COOKIE = get_cookie()
       global _CRUMB = get_crumb()
    else
        global _HEADER = _rand_header()
        global _COOKIE = get_cookie()
        global _CRUMB = get_crumb()
    end
end;


"""
    _set_cookies_and_crumb()

Checks if the global _COOKIE and _CRUMB variables are set if not it creates them. 
"""
function _set_cookies_and_crumb()
    if @isdefined _HEADER
        nothing
    else 
       global _HEADER = _rand_header()
    end
    if @isdefined _COOKIE 
        nothing
    else
        global _COOKIE = get_cookie()
    end
    if @isdefined _CRUMB 
        nothing
    else
        global _CRUMB = get_crumb()
    end
end;


_PROXY_SETTINGS = (;proxy = nothing,auth = Dict())
"""
    create_proxy_settings(p::AbstractString,user=nothing,password=nothing)

Sets the global proxy variable `_PROXY_SETTINGS::NamedTuple`. This `NamedTuple` contains a `proxy` and a `auth` field. These fields default to `nothing` and and empty `Dict` respectively.

## Arguments

 * p`::String` (Required) of the form: "http://proxy.xyz.com:8080"

 * user`::String` Username (optional) only required if proxy requires authentication. Defaults to `nothing` (no authentication needed)

 * password`::String` The password corresponding to the Username. Defaults to `nothing` (no authentication needed)

"""
function create_proxy_settings(p::AbstractString,user=nothing,password=nothing)
    if isnothing(user) || isnothing(password)
        global _PROXY_SETTINGS = (;proxy = p,auth = Dict())
    else
        global _PROXY_SETTINGS = (;proxy=p,auth=Dict("Proxy-Authorization" =>  ("Basic " * Base64.base64encode(user * ":" * password))))
    end
    return nothing
end



"""
    clear_proxy_settings()

Clears the proxy settings by setting them back to their default (no proxy configuration).
"""
function clear_proxy_settings()
    global _PROXY_SETTINGS = (;proxy = nothing,auth = Dict())
    return nothing
end

